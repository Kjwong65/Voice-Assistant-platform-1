import winston from 'winston';

// ==============================================
// LOGGER
// ==============================================

const logFormat = winston.format.combine(
  winston.format.timestamp({ format: 'YYYY-MM-DD HH:mm:ss' }),
  winston.format.errors({ stack: true }),
  winston.format.splat(),
  winston.format.json()
);

const prettyFormat = winston.format.combine(
  winston.format.timestamp({ format: 'YYYY-MM-DD HH:mm:ss' }),
  winston.format.colorize(),
  winston.format.printf(({ level, message, timestamp, service, ...metadata }) => {
    let msg = `${timestamp} [${service || 'app'}] ${level}: ${message}`;
    if (Object.keys(metadata).length > 0) {
      msg += ` ${JSON.stringify(metadata)}`;
    }
    return msg;
  })
);

export function createLogger(service: string, level: string = 'info', format: 'json' | 'pretty' = 'json') {
  return winston.createLogger({
    level,
    defaultMeta: { service },
    format: format === 'pretty' ? prettyFormat : logFormat,
    transports: [
      new winston.transports.Console(),
      new winston.transports.File({ filename: `logs/${service}-error.log`, level: 'error' }),
      new winston.transports.File({ filename: `logs/${service}-combined.log` }),
    ],
  });
}

